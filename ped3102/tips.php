<h3>Tips for Educators</h3>

<hr />

<ol class="decimal margin1em">
    <li>Know your students and the gender relationships they grew up with.</li>
    <li>Give everybody hope: They may not receive the same type of support at home.</li>
    <li>Try to understand gender roles in the traditional context: Equality doesn't exist for many students. Learn the cultures through a concept map.</li>
    <li>Boys require more attention due to behavioral and academic challenges. Statistically, higher % of male students possess exceptionalities.</li>
    <li>Boys are better at math/science vs. girls are better at reading/writing: Use a balanced approach to shorten the gap and get over it.</li>
    <li>See students after class: Some girls will be too shy to ask for help and will see you in person or figure it out on their own.</li>
</ol>

