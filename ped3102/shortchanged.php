<h3>PROs - Shortchanged</h3>

<hr />

<div class="quote">
    "Sitting in the same classroom, reading the same textbook, listening to the same teacher,<br />boys and girls receive very different educations." <a href="references.php#sadker" class="reference">(Sadker,&nbsp;1994)</a>
</div>

<br />

<div class="center color2">
    <b>Why is this?</b>
</div>

<br />

<ol class="margin1em">

    <li>
        <b>Female children with learning or behavior exceptionalities often go undiagnosed.</b>
        <p class="margin48">
            In many cases, teachers and parents attribute atypical behaviour to shyness or insecurities, behaviour that is considered "feminine." This leads to girls who are not given the resources that they need for academic success.
            However, boys are more likely to be formally diagnosed with a learning or behaviour exceptionality, and are consequently more likely to have formalized IEPs.
            Boys then, are more likely to be given tools, accommodations, and modifications to enhance their education.
            Girls are much less likely to be given the same advantages.
        </p>
    </li>
    <li>
        <b>Gender role stereotyping and gender bias lead to inequity in the classroom.</b>
        <p class="margin48">
            Schools often unconsciously reinforce and support gender stereotypes and biases, in that female students are often praised for being quiet, neat, compliant, and helpful, which are stereotypic feminine behaviours.
            On the other hand, boys are rewarded for displaying attention seeking behaviour, but girls are more likely to receive criticism for displaying assertiveness or risk-taking behaviours.
            This socialization of girls occurs because teachers tend to accept different behaviours from boys than they accept from girls, which reinforces the inequity between genders.
            This leads to female students limiting their perceptions of their academic ability and achievement, in a cycle where girls then begin shortchanging themselves.
        </p>
     </li>
     <li>
        <b>Girls do not receive a proportionate share of attention in the classroom.</b>
        <p class="margin48">
            Typically, females mature more quickly than males.
            As such, boys and girls act differently in the classroom: boys tend to be more active, assertive, outspoken and rambunctious; girls tend to be quiet and more attentive.
            Consequently, boys are easy targets for attention in class.
            Teachers tend to use classroom management strategies that involve minimizing disruption (usually occurring from the boys) by trying to actively engage students that cause the disruption (usually boys).
            Boys then, tend to be more engaged by the teacher during a lesson.
            As a result, girls often do not volunteer to participate in class or answer questions prompted by the teacher, making it more difficult for teachers to measure the comprehension and achievement for many girls.
            In addition to this, teachers often praise girls for being neat and quiet, reinforcing those qualities as "feminine."
            It is difficult to encourage girls to be more confident and assertive - qualities that educators typically strive to develop in their students - when those qualities, because they do not reinforce "feminine" ideals, are seen as "masculine" and unattractive.
        </p>
    </li>
    
</ol>

